/**
  * @brief This function handles UART4 global interrupt.
  */
void UART4_IRQHandler(void)
{
  /* USER CODE BEGIN UART4_IRQn 0 */

	if(__HAL_UART_GET_FLAG(&huart4, UART_FLAG_IDLE) && __HAL_UART_GET_IT_SOURCE(&huart4, UART_IT_IDLE))
	{
	
		HAL_UART_DMAStop(&huart4);
		HAL_UART_Receive_DMA(&huart4,usart4_dma_buff,sizeof usart4_dma_buff);
	
				if(usart4_dma_buff[0] == 0x5A) { 
            Feed_Dog(&Remote_dog); 
			if (((UART_DataPack *)usart4_dma_buff)->crc == Verify_CRC16_Check_Sum(usart4_dma_buff, sizeof(UART_DataPack) - 2)) { 
				memcpy(&RemoteData, usart4_dma_buff, sizeof(UART_DataPack)); 
			
				Remote_Control.Ey = (2047 - RemoteData.rocker[1]); 
				Remote_Control.Ex = -(2047 - RemoteData.rocker[0]);
				Remote_Control.Eangle = (2047 - RemoteData.rocker[2]);
			}
		}
		
	
	}
	
  /* USER CODE END UART4_IRQn 0 */
  HAL_UART_IRQHandler(&huart4);
  /* USER CODE BEGIN UART4_IRQn 1 */

  /* USER CODE END UART4_IRQn 1 */
}

