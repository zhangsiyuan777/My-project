float FastSqrt(float x)   //比C源代码double sqrt(double x)运行速度快近200us ， 此函数利用float比double快了9.4us
{
    float xhalf = 0.5f * x;
    
    int i = *(int*)&x;  //get bits for floating VALUE
    i = 0x5f375a86 - (i >> 1);  //gives initial guess y0
    x = *(float*)&i;   //convert bits BACK to double
    x = x * (1.5f - xhalf * x * x);  //Newton step, repeating increases accuracy

    x = 1 / x;
    
    return x;
} 
